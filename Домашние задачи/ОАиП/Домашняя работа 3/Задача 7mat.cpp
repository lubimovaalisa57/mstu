#include <iostream>
using namespace std;

int main() {
    double A[3][3] = {
        {1, 1, 1},
        {2, -1, 3},
        {-1, 4, -1}
    };
    
    // Вектор правых частей
    double B[3] = {6, 14, 2};
    
    // Решение системы методом Крамера
    
    // Вычисляем определитель основной матрицы
    double detA = A[0][0]*(A[1][1]*A[2][2] - A[1][2]*A[2][1])
                - A[0][1]*(A[1][0]*A[2][2] - A[1][2]*A[2][0])
                + A[0][2]*(A[1][0]*A[2][1] - A[1][1]*A[2][0]);
    
    // Вычисляем определитель для x
    double detX = B[0]*(A[1][1]*A[2][2] - A[1][2]*A[2][1])
                - A[0][1]*(B[1]*A[2][2] - A[1][2]*B[2])
                + A[0][2]*(B[1]*A[2][1] - A[1][1]*B[2]);
    
    // Вычисляем определитель для y
    double detY = A[0][0]*(B[1]*A[2][2] - A[1][2]*B[2])
                - B[0]*(A[1][0]*A[2][2] - A[1][2]*A[2][0])
                + A[0][2]*(A[1][0]*B[2] - B[1]*A[2][0]);
    
    // Вычисляем определитель для z
    double detZ = A[0][0]*(A[1][1]*B[2] - B[1]*A[2][1])
                - A[0][1]*(A[1][0]*B[2] - B[1]*A[2][0])
                + B[0]*(A[1][0]*A[2][1] - A[1][1]*A[2][0]);
    
   
    double x = detX / detA;
    double y = detY / detA;
    double z = detZ / detA;
    
    cout << "Решение системы:" << endl;
    cout << "x = " << x << endl;
    cout << "y = " << y << endl;
    cout << "z = " << z << endl;
    
    cout << "Проверка:" << endl;
    cout << "1 уравнение: " << x << " + " << y << " + " << z << " = " << x+y+z << " (должно быть 6)" << endl;
    cout << "2 уравнение: " << 2*x << " - " << y << " + " << 3*z << " = " << 2*x - y + 3*z << " (должно быть 14)" << endl;
    cout << "3 уравнение: -" << x << " + " << 4*y << " - " << z << " = " << -x + 4*y - z << " (должно быть 2)" << endl;
    
    return 0;
}